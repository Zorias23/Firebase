    private void updateUI(FirebaseUser user) {
        String message = "";
        if (user != null)
        {
            message = "New account created for " + user.getEmail() + " created";
        }
        AlertDialog alertDialog = new AlertDialog.Builder(this).setTitle("FB Authentication").setMessage(message).setCancelable(true)
                .setPositiveButton("ok", new DialogInterface.OnClickListener() {
                    @Override
                    public void onClick(DialogInterface dialogInterface, int i) {

                    }
                }).create();
        alertDialog.show();
        if (user!= null)
        {
            Intent intent = new Intent(this, MovieActivity.class);
            startActivity(intent);
        }
    }

